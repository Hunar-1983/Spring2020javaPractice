package day02_Variables;

public class EscapeSequencs {
    public static void main(String[] args) {
        System.out.println("\tHello");
                // (\t) using for paragraph space

        System.out.println("Cybertek \nSchool");
                // (\n) using for print statement in a new line

        System.out.println("My Favorite TV show is \"Game of Thrones\"");
                // (\") using if we want rite statement in double quote

        System.out.println("\\");
                // tp print single slash on console

        System.out.println("My favorite book is \'Java\'");
        System.out.println("My Favorite book is 'Java'");
        // (\' or ') to pring single quote in console
    }
}
